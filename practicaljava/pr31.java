package practicaljava;

public class pr31 {
    public static void main(String[] args) {
        String x = "jayesh";

        String z = x.toLowerCase();
        String y = x.toUpperCase();
        System.out.println("lowerCase:" + z);
        System.out.println("upperCase:" + y);
    }
}

/*
 * OUTPUT:
 * lowerCase:jayesh
 * upperCase:JAYESH
 */